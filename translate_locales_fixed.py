def translate_value(value, lang_dict):
    if isinstance(value, str):
        if value in lang_dict:
            return lang_dict[value]
    return value

def process_json_file(filepath, lang_dict, language_name):
    try:
        with open(filepath, "r", encoding="utf-8") as f:
            data = json.load(f)
        
        translated_count = 0
        unchanged_count = 0
        
        for key in data:
            original_value = data[key]
            translated_value = translate_value(original_value, lang_dict)
            
            if translated_value != original_value:
                translated_count += 1
                data[key] = translated_value
            else:
                unchanged_count += 1
        
        with open(filepath, "w", encoding="utf-8") as f:
            json.dump(data, f, ensure_ascii=False, indent=2)
        
        print(f"\n✓ {language_name}: Translated {translated_count} values, {unchanged_count} unchanged")
        return translated_count, unchanged_count
    except Exception as e:
        print(f"✗ Error processing {filepath}: {str(e)}")
        return 0, 0

